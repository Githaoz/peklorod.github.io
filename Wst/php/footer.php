      <h2 class="h2modal">Ken Onion – найфмейкер с мировым именем</h2>
      <hr class="hr_modal"/>                       
<p>Кен Онион — американский изобретатель, конструктор, ножевой дизайнер, имя которого прогремело благодаря созданию механизма assisted-opening mechanism (патент SpeedSafe).
 Технология применяется при производстве складных ножей — простое устройство позволяет легким движением открыть лезвие. Случилось это в 1997 году, после чего Кен стал мировой знаменитостью.
</p> <p>Услугами авторитетного мастера пользуются известные компании в области производства точильных инструментов — Kershaw, Santa Fe Stoneworks, Gerber, Benchmade, Buck Knives, Zero Tolerance, CRKT, Ontario. 
</p> <p>Индивидуальный стиль, безупречность линий, идеальная система ножевых замков делают работы найфмейкера эксклюзивными. Охотничьи ножи  с фиксатором клинка в исполнении Ониона становятся произведением искусства. Авторские ножи Кена Ониона есть в коллекциях таких персон, как Стивен Сигал, Памела Андерсон, Николас Кейдж, Зигги Марли.
</p> <p>Уровень мастерства профессионала по достоинству оценен экспертами — в коллекции Кена награды «Нож года 2005», «Нож года 2007», присутствие в Зале Славы журнала Blade Magazine, премии выставки Blade Show,.
</p> <p>Онион зарегистрировал 36 патентов в области ножевого производства, в том числе на дизайнерские разработки, выпуск ножевых замков и механизмов. </p>
